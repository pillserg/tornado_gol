def parse_garder(s):
    world = map(lambda s: [1 if x == '*' else 0 for x in s], filter(lambda x: not x.startswith('#'), s.split('\n')))
    cells = []
    for y, row in enumerate(world):
        for x, cell in enumerate(row):
            if cell:
                cells.append((y,x))
    return cells


